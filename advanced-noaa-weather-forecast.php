class NOAA_Seven_Days_Forecast extends WP_Widget {	
	/** constructor */
	function __construct() {
		parent::WP_Widget( /* Base ID */'noaa_7_days_forecast', /* Name */'NWF: 7 Days Forecast', array( 'description' => 'Display 7 days forecast for a specific location in US.' ) );
	}

	/** @see WP_Widget::widget */
	function widget( $args, $instance ) {
	global $wpdb;	
		extract( $args );
		
		$zipcode = $instance['zip_code']; //"rextangtw";
		$title = $instance['page_title'];
		
		$sqlGetLocation=$wpdb->get_results("select * from wp_noaa where Location = '" . $zipcode . "'");
		
		$xml = simplexml_load_file('http://forecast.weather.gov/MapClick.php?lat=' . $sqlGetLocation[0]->Lat . '000&lon=-' . $sqlGetLocation[0]->Lon . '000&FcstType=xml');
		
		echo $before_widget;
		echo $before_title . $title . $after_title;
		echo $zipcode;
		
		//print_r( $xml );
		?>	
			<table style=" width: 100%; border-radius: 3px ; -moz-border-radius: 3px ;">
		<?php 	
			$i=0;
			while( $i < count($xml->period) ) 
			{
		?>
			<tr>
			 <td style="width: 35%; padding: 5px; text-align: center;"><br/><img src ="http://mobile.wrh.noaa.gov/weather/images/fcicons/<?php echo "{$xml->period[$i]->image}"; ?>" width="50px;"><br/><br/><b><?php echo $xml->period[$i]->weather; ?></b><br/><b>Temp: <?php echo $xml->period[$i]->temp; ?> &#176;F</b></td>
			 <td style="padding: 5px; text-align: justify; margin-top: 0px !important;"><b><?php echo $xml->period[$i]->valid; ?></b><br/><p style="font-size: 11px;"><?php echo $xml->period[$i]->text; ?></p></td>			 
			</tr>
			<tr><td colspan="2"><hr></td></tr>
		<?php	
			$i++;
			} 
		?>
			</table>
		<?php		
		echo $after_widget;
	}

	/** @see WP_Widget::update */
	function update( $new_instance, $old_instance ) {
		$instance = $old_instance;
		$instance['page_title'] = strip_tags($new_instance['page_title']);
		$instance['zip_code'] = strip_tags($new_instance['zip_code']);
		return $instance;
	}

	/** @see WP_Widget::form */
	function form( $instance ) {
	global $wpdb;
		if ( $instance ) {
			$title = esc_attr( $instance[ 'page_title' ] );
			$zipcode = esc_attr( $instance[ 'zip_code' ] );
		}
		else {
			$title = __( '7 Days Forecast', 'text_domain' );
			$zipcode = "";
		}
		?>
		
		<p>
		<label for="<?php echo $this->get_field_id('page_title'); ?>"><?php _e('Widget Title:'); ?></label> 
		<input class="widefat" id="<?php echo $this->get_field_id('page_title'); ?>" name="<?php echo $this->get_field_name('page_title'); ?>" type="text" value="<?php echo $title; ?>" />
		</p>
		
		<p>
		<label for="<?php echo $this->get_field_id('zip_code'); ?>"><?php _e('Location:'); ?></label> 
		<select class="widefat" id="<?php echo $this->get_field_id('zip_code'); ?>" name="<?php echo $this->get_field_name('zip_code'); ?>">
		 <?php 
		  $sqlGetLocation=$wpdb->get_results("select * from wp_noaa");
		   $i=0;
		  while( $i < count( $sqlGetLocation ) )
		  {
		  ?>
		  	<option value = "<?php echo "{$sqlGetLocation[$i]->Location}"; ?>" <?php selected( $zipcode, $sqlGetLocation[$i]->Location ); ?> ><?php echo $sqlGetLocation[$i]->Location; ?></option>
		  <?php
		  	$i++;
		  }
		 ?>		 
		</select>
		</p>
		
		
		<?php 
	}

}

